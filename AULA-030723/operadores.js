/* 
    Operadores Aritméticos:
    + Adição
    - Subtração
    * Multiplicação
    / Divisão
    ** Exponenciação
    % Resto da divisão
*/


const num1 = 5;
const num2 = 8;
const num3 = 4.5;

let result;

result = num1 + num2;
console.log("Resultado: " + result);

result = num2 - num1;
console.log("Resultado: " + result);

result = num1 * num2;
console.log("Resultado: " + result);

result = num1 / num2;
console.log("Resultado: " + result);

result = num1 ** num2;
console.log("Resultado: " + result);

result = num2 % num3;
console.log("Resultado: " + result);

result = num2 % num1;
console.log(`Resultado: ${result}`);